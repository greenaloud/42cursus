char	*ft_strrchr(const char *s, int c)
{
	int	idx;

	idx = 0;
	while (s[idx])
		idx++;
	while (idx >= 0)
	{
		if (s[idx] == c)
			return ((char *)(s + idx));
		idx--;
	}
	return (0);
}
